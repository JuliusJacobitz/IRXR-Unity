import socket
import sys
import argparse
import time
sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
sock.setsockopt(socket.SOL_SOCKET, socket.SO_BROADCAST, 1)
while True:
    time.sleep(1)
    sock.sendto("Hello from windows".encode(), ("255.255.255.255", 7740))

sock.close()
